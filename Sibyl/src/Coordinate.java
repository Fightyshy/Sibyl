class Coordinate {
	public int xCoord;
	public int yCoord;
	
	public Coordinate(){
		
	}
	
	public Coordinate(int x, int y){
		xCoord = x;
		yCoord = y;
	}
	
	public void setXCoord(int x){
		xCoord = x;
	}
	
	public int getXCoord(){
		return xCoord;
	}
	
	public void setYCoord(int y){
		yCoord = y;
	}
	
	public int getYCoord(){
		return yCoord;
	}
}
